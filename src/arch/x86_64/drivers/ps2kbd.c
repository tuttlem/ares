/* Initializes the keyboard driver */
static int ps2kbd_init();

/* Unloads the keyboard driver */
static void ps2kbd_term();


/**
 * For a given scan code value, this function will lookup the
 * corresponding key code
 * @param {uint8_t} scan_code The scan code to translate
 * @return {uint8_t} The resulting key code
 */
uint8_t kbd_get_key_code(uint8_t scan_code) {
    int i;

    for (i = 0; i < KBD_S2K_TABLE_SIZE; i ++) {
        if (kbd_s2k_pairs[i].scan_code == scan_code) {
            return kbd_s2k_pairs[i].key_code;
        }
    }

    return 0x00;
}

/* The keyboard IRQ handler routine */
void ps2kbd_handler(struct _registers regs);

/* The keyboard IRQ handler routine */
void ps2kbd_handler(struct _registers regs) {
  /* read the scan code from the keyboard - take note: this is not the
     ASCII code of the character */
  uint8_t scan_code = inb(KBD_DATA_PORT);
  uint8_t key_code = kbd_get_key_code(scan_code);

  if (key_code) {
    kbd_buffer[kbd_head] = key_code;
    kbd_head = (kbd_head + 1) % KBD_BUFFER_SIZE;
  }
}

uint8_t kbd_get_key() {
    uint8_t key_code;

    if (kbd_head == kbd_tail) {
        return 0x00;
    }

    key_code = kbd_buffer[kbd_tail];
    kbd_tail = (kbd_tail + 1) % KBD_BUFFER_SIZE;

    return key_code;
}

driver_t ps2kbd_driver = {
    .name = "ps2kbd",
    .init = ps2kbd_init,
    .term = ps2kbd_term,
};

static int ps2kbd_read(device_t* dev, char* buf, int len) {
    int i = 0;
    while (i < len) {
        uint8_t ch = kbd_get_key();
        if (ch == 0x00) break;
        buf[i++] = ch;
    }
    return i;
}

device_t ps2kbd_device = {
    .name = "ps2kbd0",
    .type = DEVICE_CHAR,
    .read = ps2kbd_read,
    .write = 0, // Not writable
    .driver_data = NULL
};


/* Initializes the keyboard driver */
static int ps2kbd_init() {
    /* install an interrupt handler that will listen to IRQ1 which
       is the keyboard */
    interrupt_register_handler(IRQ1, ps2kbd_handler);

    device_register(&ps2kbd_device);

    return 0;
}

/* Unloads the keyboard driver */
static void ps2kbd_term() {
    /* remove the keyboard handler */
    interrupt_register_handler(IRQ1, NULL);
}
